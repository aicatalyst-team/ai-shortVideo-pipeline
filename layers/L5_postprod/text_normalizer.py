def to_simplified_zh(text: str) -> str:
    """Best-effort Traditional Chinese to Simplified Chinese normalization."""
    raw = str(text or "")
    if not raw:
        return ""
    try:
        from opencc import OpenCC  # type: ignore

        return OpenCC("t2s").convert(raw)
    except Exception:
        normalized = raw
        for src, dst in _PHRASE_REPLACEMENTS:
            normalized = normalized.replace(src, dst)
        return normalized.translate(_CHAR_REPLACEMENTS)
